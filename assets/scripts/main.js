function loadJSON(callback) {

   var xobj = new XMLHttpRequest();
       xobj.overrideMimeType("application/json");
   xobj.open('GET', 'letters.json', true); // Replace 'my_data' with the path to your file
   xobj.onreadystatechange = function () {
         if (xobj.readyState == 4 && xobj.status == "200") {
           // Required use of an anonymous callback as .open will NOT return a value but simply returns undefined in asynchronous mode
           callback(xobj.responseText);
         }
   };
   xobj.send(null);
}


function tile(context, imgs, tiles, character) {
  for(c=0; c<character.width; c++) {
    for(r=0; r<character.height; r++) {
      context.drawImage(imgs[character.data[r][c]],
      offset.width + (imgs[character.data[r][c]].width)*c, offset.height + (imgs[character.data[r][c]].height)*r,
      imgs[character.data[r][c]].width, imgs[character.data[r][c]].height);
    }
  }
  offset.width += character.width * 16;
}
